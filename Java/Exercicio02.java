public class Exercicio02{
    public static void main(String[] args) {
        
        System.out.println("Exercicio 02");
        System.out.println("Crie variaveis para armazenar os seguintes valores e imprima no console:");
        long numLong =  2147483649l; 
        System.out.println("Primeiro valor: " + numLong);
        byte numByte = 120; 
        System.out.println("Segundo valor: " + numByte); 
        short numShort = 150; 
        System.out.println("Terceiro valor: " + numShort); 
        String texto = "Bem vindo Devs!";
        System.out.println("Quarto valor: " + texto);
        String texto2 = "0x2202"; 
        System.out.println("Quinto valor: " + texto2);
        float numFloat = 99999.987f; 
        System.out.println("Sexto valor: " + numFloat);
    }
}